def test_ring_and_edge_marks() -> None:
    grid, scene = make_scene()
    cell = grid.cell_at((2, 2))
    assert cell is not None
    scene.add(CellMark(cell, ring=True), EdgeMark(grid.edge(cell, "e")))
    svg = SvgRenderer().render(scene)
    # Ring: halo-filled open circle at the cell center (96, 96)
    ring = re.search(r'<circle cx="96" cy="96" [^>]*/>', svg)
    assert ring is not None
    assert 'r="9"' in ring.group(0) and 'fill="#ffffff"' in ring.group(0) and 'stroke-width="3.5"' in ring.group(0)
    # Default dot at the east edge midpoint (128, 96)
    dot = re.search(r'<circle cx="128" cy="96" [^>]*/>', svg)
    assert dot is not None
    assert 'r="4"' in dot.group(0) and 'fill="#1d6ae5"' in dot.group(0)


