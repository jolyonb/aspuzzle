def test_edge_weights_scale_stroke() -> None:
    grid, scene = make_scene()
    cells = list(grid.all_cells())
    scene.add(
        EdgeSegment(grid.edge(cells[0], "e")),
        EdgeSegment(grid.edge(cells[5], "s"), weight=EdgeWeight.HEAVY),
    )
    svg = SvgRenderer().render(scene)
    widths = [float(w) for w in re.findall(r'<path class="edge"[^>]*stroke-width="([^"]+)"', svg)]
    assert len(widths) == 2
    assert widths[1] == 2 * widths[0]


def test_edge_segments_chain_into_paths() -> None:
    grid, scene = make_scene()
    cell = grid.cell_at((2, 2))
    assert cell is not None
    corner_cell = grid.cell_at((1, 4))
    assert corner_cell is not None
    # A fully-circled cell and, in a different color, an L-corner
    scene.add(*(EdgeSegment(grid.edge(cell, direction)) for direction in "nesw"))
    scene.add(
        EdgeSegment(grid.edge(corner_cell, "s"), color=PaletteColor.RED),
        EdgeSegment(grid.edge(corner_cell, "w"), color=PaletteColor.RED),
    )
    svg = SvgRenderer().render(scene)
    paths = re.findall(r'<path class="edge" d="([^"]+)"', svg)
    assert len(paths) == 2  # one per style, not one per segment
    closed = [d for d in paths if d.endswith("Z")]
    open_runs = [d for d in paths if not d.endswith("Z")]
    assert len(closed) == 1 and closed[0].count("M") == 1  # the loop: one closed subpath
    assert len(open_runs) == 1 and open_runs[0].count("L") == 2  # the corner: one chained run


def test_cell_path_runs_center_to_edge_midpoints() -> None:
    grid, scene = make_scene()
    cell = grid.cell_at((2, 2))
    assert cell is not None
    scene.add(CellPath(cell, frozenset({"e", "s"}), color=PaletteColor.CYAN))
    svg = SvgRenderer().render(scene)
    match = re.search(r'<path class="path" d="([^"]+)"', svg)
    assert match is not None
    # Cell (2,2) at 64px/cell: center (96, 96), east midpoint (128, 96),
    # south midpoint (96, 128); one center-to-midpoint stroke per direction
    assert match.group(1) == "M 96 96 L 128 96 M 96 96 L 96 128"


