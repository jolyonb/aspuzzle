def make_scene(style: SceneStyle | None = None) -> tuple[Grid, Scene]:
    grid = rect_3x4()
    return grid, Scene(grid, style=style if style is not None else SceneStyle())


def view_box(svg: str) -> tuple[float, float, float, float]:
    match = re.search(r'viewBox="([^"]+)"', svg)
    assert match is not None
    a, b, c, d = (float(part) for part in match.group(1).split())
    return a, b, c, d


def test_document_shell() -> None:
    _grid, scene = make_scene()
    svg = SvgRenderer().render(scene)
    lines = svg.splitlines()
    assert lines[0] == "<!-- Generated by ASPuzzle https://github.com/jolyonb/aspuzzle -->"
    assert lines[1].startswith('<svg xmlns="http://www.w3.org/2000/svg"')
    assert svg.endswith("</svg>\n")
    # Default substrate: hairline lattice and heavy frame, no dots
    assert 'class="lattice"' in svg
    assert 'class="frame"' in svg
    assert "<circle" not in svg
    # 4 cols x 3 rows at 64px/cell plus a quarter-cell margin each side
    assert view_box(svg) == (-16.0, -16.0, 288.0, 224.0)
    # The theme's colors assume the background they declare: an explicit
    # viewBox-covering white rect, ahead of everything drawn
    assert '<rect x="-16" y="-16" width="288" height="224" fill="#ffffff"/>' in svg


def test_render_is_deterministic() -> None:
    def build() -> str:
        grid, scene = make_scene()
        cells = list(grid.all_cells())
        scene.add(CellFill(cells[5], PaletteColor.GREEN), CellGlyph(cells[0], Glyph("7")))
        return SvgRenderer().render(scene)

    assert build() == build()


def test_glyph_text_is_escaped() -> None:
    grid, scene = make_scene()
    scene.add(CellGlyph(next(grid.all_cells()), Glyph("<")))
    svg = SvgRenderer().render(scene)
    assert ">&lt;</text>" in svg


def test_emoji_class_skips_halo_and_shrinks() -> None:
    # Emoji get their own class: smaller (their ink fills the em square)
    # and unstroked — Firefox drops color-emoji glyphs from stroked text
    grid, scene = make_scene()
    cells = list(grid.all_cells())
    scene.add(CellGlyph(cells[0], Glyph("5")), CellGlyph(cells[1], Glyph("⛺")))
    svg = SvgRenderer().render(scene)
    assert re.search(r'<text [^>]*class="bigtext halo"[^>]*>5</text>', svg) is not None
    assert re.search(r'<text [^>]*class="emoji"[^>]*>⛺</text>', svg) is not None
    assert ".emoji { font-size: 40px;" in svg


def test_svg_glyph_variant_wins() -> None:
    grid, scene = make_scene()
    scene.add(CellGlyph(next(grid.all_cells()), Glyph("A", svg="⛺")))
    svg = SvgRenderer().render(scene)
    assert ">⛺</text>" in svg
    assert ">A</text>" not in svg


def test_provenance_styles_text() -> None:
    grid, scene = make_scene()
    cells = list(grid.all_cells())
    scene.add(
        CellGlyph(cells[0], Glyph("5"), provenance=Provenance.GIVEN),
        CellGlyph(cells[1], Glyph("6")),
    )
    svg = SvgRenderer().render(scene)
    given = re.search(r'<text [^>]*data-provenance="given"[^>]*>5</text>', svg)
    derived = re.search(r'<text [^>]*data-provenance="derived"[^>]*>6</text>', svg)
    assert given is not None and derived is not None
    assert 'fill="#000000"' in given.group(0)
    assert 'fill="#1d6ae5"' in derived.group(0)


